"""
skills.py — Reflection-bot skill functions.

Three-phase reflection flow (Этажерка):

  Phase 1 · Анализ ситуации
      Layer 0 — Вход в неделю (цели / задачи)
      Layer 1 — Ход действий
      Layer 2 — Результаты и точки остановки
      Summary — промежуточный итог фазы 1

  Phase 2 · Рефлексивное действие
      Episode selection — выбор эпизода
      Layer 0 — Деятельность и средства
      Layer 1 — Взаимодействие и распределение действий
      Layer 2 — Способ понимания (мышление)
      Summary — промежуточный итог фазы 2

  Phase 3 · Проектирование нового способа действия
      Layer 0 — Новый способ понимания
      Layer 1 — Новый способ участия (взаимодействие)
      Layer 2 — Конкретные новые действия и средства
      Summary — финальный итог

Every skill is an async function.
Return value: dict with at minimum:
    "response"       – str  – text to send to the user
    "layer_complete" – bool – True when the layer acceptance criteria are met
    "layer_data"     – str | None – extracted data to carry to the next layer
                        (only populated when layer_complete is True)
"""

import logging
import json
import re

logger = logging.getLogger(__name__)


# ─────────────────────────────────────────────────────────────────────────────
# Internal helpers
# ─────────────────────────────────────────────────────────────────────────────

def _parse_llm_json(raw: str) -> dict:
    """
    Parse a JSON dict from raw LLM output.
    Handles markdown code-fences and embedded JSON objects.
    Falls back to returning raw text as `response` when all parsing fails.
    """
    _fallback = {
        "response": "Произошла техническая ошибка. Пожалуйста, повтори своё сообщение.",
        "layer_complete": False,
        "layer_data": None,
    }
 
    if not raw:
        logger.warning("LLM вернул пустой ответ.")
        return _fallback.copy()
 
    logger.info("LLM raw output (first 500 chars): %s", raw)
 
    # Strip possible markdown fences (```json … ``` or ``` … ```)
    clean = re.sub(r"```(?:json)?", "", raw).strip().rstrip("`").strip()
    logger.debug("LLM output after cleaning markdown (first 500 chars): %s", clean)
 
    def _try_parse(s: str) -> dict | None:
        try:
            result = json.loads(s)
            if isinstance(result, dict):
                result.setdefault("layer_complete", False)
                result.setdefault("layer_data", None)
                logger.info("JSON успешно распарсен: %s", result)
                return result
        except json.JSONDecodeError as e:
            logger.warning("JSONDecodeError: %s | Строка: %s", e, s[:300])
            return None
 
    # Attempt 1 – direct parse
    parsed = _try_parse(clean)
    if parsed:
        return parsed
 
    # Attempt 2 – find the first {...} block
    m = re.search(r"\{.*\}", clean, re.DOTALL)
    if m:
        logger.debug("Пробуем распарсить первый найденный JSON-блок: %s", m.group()[:300])
        parsed = _try_parse(m.group())
        if parsed:
            return parsed
 
    logger.warning("JSON parse failed. First 300 chars: %s", raw[:300])
    # Return the raw text as a response so the user sees something meaningful
    return {"response": raw.strip(), "layer_complete": False, "layer_data": None}


# ═════════════════════════════════════════════════════════════════════════════
#  PHASE 1 — SITUATION ANALYSIS  (Анализ ситуации)
